from sudoku import Sudoku
from time import time


def profile(mode, puzzle):
    sudoku = Sudoku(puzzle)
    print('Puzzle (%s):' % mode)
    sudoku.print()
    s = time()
    solved = sudoku.solve()
    f = time()
    if solved:
        print('Solution:')
        sudoku.print()
        print('Solved in %.2f seconds' % (f - s))
    else:
        print('Can\'t be solved!')


